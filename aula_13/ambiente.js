// Estrutura de repetição com teste lógico no início.

// while verifica se o argumento é verdadeiro e executa incrementando mais um com o c++ até não ser mais verdade.
var c = 1

while(c <= 10){
    console.log(`passo ${c}`)
    c++
}

// Estrutura de repetição com teste lógico no final.

do {
    console.log(`passo ${c}`)
    c++
}while(c <= 10)